package ClassMethod;

public class SimpleCalculator {
	static final double PI = Math.PI;
	
	static final double add(double n1, double n2) {
		return n1 + n2;
	}
	static final double min(double n1, double n2) {
		return n1 - n2;
	}
	static final double mul(double n1, double n2) {
		return n1 * n2;
	}
	static final double div(double n1, double n2) {
		return n1 / n2;
	}

}
