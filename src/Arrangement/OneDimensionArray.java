package Arrangement;

public class OneDimensionArray {

	public static void main(String[] args) {
		//Create 1dimension array in type int with length to 5
		int[] arr1 = new int[5];
		//Create 1dimension array in type double with length to 7
		double[] arr2 = new double[7];
		//Create 1dimension array in type reference variables with length to 9
		float[] arr3 = new float[9];
		
		System.out.println("배열 arr1의 길이 : " + arr1.length);
		System.out.println("배열 arr2의 길이 : " + arr2.length);
		System.out.println("배열 arr3의 길이 : " + arr3.length);
		
	}

}
